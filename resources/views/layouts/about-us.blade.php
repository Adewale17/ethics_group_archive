  <div class="container my-5">
    <h2 class="text-center mb-4">About Nigerian Heritage Archive</h2>
    <div class="row justify-content-center">
      <div class="col-lg-10">
        <p class="lead">
          The <strong>Nigerian Heritage Archive</strong> is a digital initiative aimed at preserving and promoting the rich cultural heritage of Nigeria’s diverse ethnic groups. We believe that every tribe, language, and tradition contributes to the nation’s identity, and it is our mission to document, celebrate, and share this wealth of knowledge with future generations.
        </p>

        <h4>Our Mission</h4>
        <p>
          Our mission is to create a centralized and accessible online repository where people can explore detailed information about Nigerian ethnic groups, including their history, culture, language, festivals, and values. By doing this, we seek to bridge the knowledge gap between generations and foster mutual respect and appreciation among the country's many cultures.
        </p>

        <h4>What We Offer</h4>
        <ul>
          <li>Profiles of major ethnic groups such as Yoruba, Hausa, Igbo, and others.</li>
          <li>Digital archives of traditional practices, festivals, crafts, and beliefs.</li>
          <li>Resources for researchers, students, and anyone interested in Nigerian heritage.</li>
          <li>Opportunities for contributions from individuals and communities to share untold stories.</li>
        </ul>

        <h4>Why It Matters</h4>
        <p>
          In a rapidly modernizing world, many indigenous traditions are at risk of being forgotten. The Nigerian Heritage Archive provides a platform where cultural identity can thrive digitally. It serves not just as a database, but as a living monument to Nigeria's roots, ensuring that our diversity is not lost but preserved and appreciated globally.
        </p>

        <h4>Join Us</h4>
        <p>
          Whether you're a student, researcher, enthusiast, or community elder, your input is valuable. We invite you to explore, learn, and contribute to this growing archive as we honor Nigeria's cultural legacy one tribe at a time.
        </p>
